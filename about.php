    document.addEventListener('DOMContentLoaded', function() {
        const counters = document.querySelectorAll('.purecounter');

        counters.forEach(counter => {
            const start = parseInt(counter.getAttribute('data-purecounter-start') || '0', 10);
            const end = parseInt(counter.getAttribute('data-purecounter-end') || '0', 10);
            const duration = parseInt(counter.getAttribute('data-purecounter-duration') || '1', 10);

            const updateCounter = () => {
                const step = (end - start) / (duration * 60); // Calculate the step value
                const current = parseInt(counter.textContent, 10);

                if (current < end) {
                    counter.textContent = Math.ceil(current + step);
                    setTimeout(updateCounter, 1000 /
                        60); // Update every frame (60 times per second)
                }
            };

            updateCounter(); // Start the counter
        });
    });
    </script>

